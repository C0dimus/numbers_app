extension UIColor {
    class var lavaRed: UIColor {
        return UIColor(red: 207/255, green: 16/255, blue: 32/255, alpha: 1)
    }
    
    class var niceBlue: UIColor {
        return UIColor(red: 16/255, green: 128/255, blue: 207/255, alpha: 1)
    }
    
}
